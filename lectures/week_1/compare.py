x = int(input("What's x? "))
y = int(input("What's y? "))

# conditions
if x < y:
    print("x is less than y")

elif x > y:
    print("x is greater than y")

else:
    print("x is equal to y")

# or - just checking for equality
if x < y or x > y:
    print("x is not equal to y")

else:
    print("x is equal to y")

# checking if not equal
if x != y:
    print("x is not equal to y")

else:
    print("x is equal to y")


